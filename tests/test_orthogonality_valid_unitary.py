"""Test that col(Phi_lead) ⊥ col(Phi_const) holds for valid full unitary polynomials.

This verifies that when we have a valid N×N unitary polynomial U(z) generated by
forward U(N)-QSP, the leading and constant coefficients satisfy the orthogonality
constraint required by the recursive reduction algorithm.
"""

import numpy as np
import pytest

from src.utils import unit_circle_points
from tests.conftest import make_random_achievable


def test_orthogonality_for_valid_full_unitary():
    """Test that col(U_d) ⊥ col(U_0) for a valid full N×N unitary polynomial.
    
    For a valid unitary polynomial U(z) generated by forward U(N)-QSP,
    the leading coefficient U_d and constant coefficient U_0 should satisfy
    U_d† U_0 = 0 (orthogonal column spaces).
    """
    # Generate a valid full N×N unitary polynomial via forward pass
    d, N = 8, 8
    rng = np.random.default_rng(42)
    
    # Random unitaries in U(N)
    def random_unitary(size):
        A = rng.standard_normal((size, size)) + 1j * rng.standard_normal((size, size))
        Q, _ = np.linalg.qr(A)
        return Q
    
    R_list = [random_unitary(N) for _ in range(d + 1)]
    ell_list = rng.integers(1, N // 2 + 1, size=d).tolist()
    
    # Forward simulation
    K = max(8 * (d + 1), 512)
    z_pts = unit_circle_points(K)
    
    U_vals = np.zeros((K, N, N), dtype=np.complex128)
    for ki in range(K):
        z = z_pts[ki]
        M = R_list[0].copy()
        for k in range(1, d + 1):
            ell = ell_list[k - 1]
            C_k = np.eye(N, dtype=np.complex128)
            C_k[:ell, :ell] *= z
            M = R_list[k] @ C_k @ M
        U_vals[ki] = M
    
    # Recover polynomial coefficients via IFFT
    all_coeffs = np.fft.fft(U_vals, axis=0) / K
    U_coeffs = all_coeffs[:d + 1]
    
    # Extract leading and constant coefficients
    U_lead = U_coeffs[d]  # Leading coefficient (degree d)
    U_const = U_coeffs[0]  # Constant coefficient (degree 0)
    
    # Check orthogonality: U_d† U_0 should be close to zero
    cross_product = U_lead.conj().T @ U_const
    orthogonality_error = np.linalg.norm(cross_product, "fro")
    
    print(f"\nValid full unitary (d={d}, N={N}):")
    print(f"  col(U_lead) ⊥ col(U_const) error: {orthogonality_error:.3e}")
    
    # For a valid unitary polynomial, this should be very small
    assert orthogonality_error < 1e-10, (
        f"Orthogonality violated for valid unitary: {orthogonality_error:.3e}"
    )


